    <h1>Tech Overview</h1>
    <p>Welcome to the tech page! Here’s an overview of the technologies and structure behind this project.</p>

    <h2>Technologies Used</h2>
    <ul>
        <li><strong>Language:</strong> PHP 8.x</li>
        <li><strong>Server:</strong> PHP built-in server / Apache / Nginx</li>
        <li><strong>Database:</strong> MySQL or MariaDB</li>
        <li><strong>Frontend:</strong> HTML5, CSS3, JavaScript</li>
        <li><strong>Version Control:</strong> Git + GitHub</li>
        <li><strong>Dependencies:</strong> Composer (PSR-4 autoloading)</li>
    </ul>

    <h2>Project Structure</h2>
    <pre>
/public      → web root (index.php, assets)
/src         → PHP source (controllers, models)
/views       → templates and page views
/partials    → reusable page sections
routes.php   → route definitions
helpers.php  → utility functions
    </pre>

    <h2>Best Practices</h2>
    <ul>
        <li>Follows PSR-12 coding standards</li>
        <li>Uses prepared statements for database queries</li>
        <li>Separates business logic and presentation</li>
        <li>Validates and sanitizes all input</li>
        <li>Uses namespaces and autoloading</li>
    </ul>

    <h2>Future Improvements</h2>
